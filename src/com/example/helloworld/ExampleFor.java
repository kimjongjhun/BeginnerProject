package com.example.helloworld;

public class ExampleFor {

    public static void main(String[] args) {

        for (int i = 0; i >= (-10); i--) {
            System.out.println("i= " + i);
            // this one will select all numbers between (-10) and 0
        }
        for (int i = 4; i <= 22; i++) {
            System.out.println("i= " + i);
            // this will select all the numbers between 4 and 22
        }
    }
}
